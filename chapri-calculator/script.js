function animatePercentage(target) {
    let current = 0;
    const increment = target / 50;
    const timer = setInterval(() => {
        current += increment;
        if (current >= target) {
            current = target;
            clearInterval(timer);
        }
        document.getElementById('percentage').textContent = Math.round(current) + '%';
    }, 30);
}

